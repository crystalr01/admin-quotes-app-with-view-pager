package com.rameshwar.theuntoldlinesadmin;

public class NoticeData2 {

    String title,image;

    public NoticeData2() {
    }

    public NoticeData2(String title, String image) {
        this.title = title;
        this.image = image;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

}
